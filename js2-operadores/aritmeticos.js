// Operadores aritmeticos
// 
// +     -     *     /    %

const a = 7;
const b = 3;

const soma = a + b; // o simbolo + serve 2 propositos: operador de soma aritmetica
                    // tambem e o operador de concatenacao
const sub = a - b;
const multi = a * b;
const divi = a / b;
const resto = a % b;

console.log(soma);
console.log(sub);
console.log(multi);
console.log(divi);
console.log(resto);